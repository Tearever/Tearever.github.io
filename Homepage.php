<p class="w3-left w3-border w3-border-black w3-black">
I find Taxi Driver a perfect example of a movie that uses narration to lead the audience through the plot. Robert De Niro's character Travis Bickle is a lonely and despondent taxi driver whose detachment from reality intensifies as the movie unfolds. The 
filmmakers of Taxi Driver decided to have Travis Bickle’s voice-over commentary as the primary means of illustrating his deteriorating mental state. This allows the audience to get a front-row seat into the disturbed mind of Travis Bickle. The film utilizes this
 approach as a way to provide context for Travis’s actions and motivations. This becomes especially helpful when his perspective shapes his perception of the world. He often brings up that New York City is decaying, its corrupt inhabitants, and the need for someone
  to ‘clean up the streets’. This insight can allow the audience to follow his descent into violence and vigilantism, ultimately enhancing our comprehension of Travis as a character.</p> 
